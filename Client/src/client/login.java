    private void generateCaptcha() {
       captcha = new Captcha.Builder(200, 50)
            .addText()
            .addBackground()
            .build();

    // 필터를 적용한 이미지를 가져오도록 수정
         BufferedImage captchaImage = captcha.getImage();
    ImageIcon captchaIcon = new ImageIcon(captchaImage);
    captchaImageLabel.setIcon(captchaIcon);

    
    }
    
    
