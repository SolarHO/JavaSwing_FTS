    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
       String enteredID = idfield.getText();
    String enteredPassword = passwdfield.getText();
    String enteredCaptcha = captchafield.getText();
    
    if (validateCaptcha(enteredCaptcha)) {
        try {
            String query = "SELECT * FROM filemember WHERE id = ? AND passwd = ?";   //db에서 일치하는 id와 비밀번호를 찾음 
            try (PreparedStatement preparedStatement = dbConnection.prepareStatement(query)) {
                preparedStatement.setString(1, enteredID);
                preparedStatement.setString(2, enteredPassword);
                
                try (ResultSet rs = preparedStatement.executeQuery()) {
                    if (rs.next()) {
                        // 로그인 성공 시 Main_Client를 실행
                        SwingUtilities.invokeLater(() -> {
                            try {
                                Main_Client mainClient = new Main_Client();
                                Dimension frameSize = mainClient.getSize();
                                Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
                                mainClient.setLocation((screenSize.width - frameSize.width) / 2, (screenSize.height - frameSize.height) / 2);
                                mainClient.setVisible(true);
                                mainClient.setTxtNameText(rs.getString("name")); //메인클라이언트부분의 TxtName칸에 계정의 이름을 세팅
                                mainClient.setSession(rs.getString("name"));
                            } catch (Exception e) {
                                e.printStackTrace();
                            }
                        });
                        
                        
                        // 로그인 성공 메시지 출력
                        JOptionPane.showMessageDialog(this, "로그인 성공!");
                        // 로그인 폼 닫기
                        dispose();
                    } else {
                        // 일치하는 사용자가 없으면 로그인 실패 메시지 출력
                        JOptionPane.showMessageDialog(this, "로그인 실패. 아이디 또는 비밀번호를 확인하세요.");
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    } else {
        // 캡차 검증 실패 메시지 출력
        JOptionPane.showMessageDialog(this, "캡차를 올바르게 입력하세요.");
    }

    // 로그인 후에는 다시 캡차 갱신
    generateCaptcha();
    }//GEN-LAST:event_btnloginActionPerformed

